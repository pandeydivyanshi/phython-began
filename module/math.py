import math

print("Square root of 16:", math.sqrt(16))                  # 4.0
print("2 raised to the power of 3:", math.pow(2, 3))        # 8.0
print("Factorial of 5:", math.factorial(5))                 # 120
print("GCD of 12 and 15:", math.gcd(12, 15))                # 3
print("LCM of 12 and 15:", math.lcm(12, 15))                # 60 (Python 3.9+)
print("Ceil of 4.2:", math.ceil(4.2))                       # 5
print("Floor of 4.8:", math.floor(4.8))                     # 4
print("Value of pi:", math.pi)                              # 3.141592653589793
print("Value of e:", math.e)                                # 2.718281828459045
print("Sine of π/2:", math.sin(math.pi / 2))                # 1.0
print("Cosine of π:", math.cos(math.pi))                    # -1.0
print("Tangent of π/4:", math.tan(math.pi / 4))            # 1.0
print(pow(2, 3))  # 8                                            ##inbuilt-in pow function
print(math.pow(2, 3))                                        # 8.0 (math module's pow function)
print("Logarithm base 10 of 100:", math.log10(100))        # 2.0
print("Natural logarithm of e:", math.log(math.e))          # 1.0
print(abs(-5.88667))                                              # 5 (absolute value) inbuilt-in function
print(math.fabs(-5.78563))                                        # 5.0 (absolute value using math module)
print(math.degrees(math.pi))                                    # 180.0 (radians to degrees)
print(math.radians(180))                                        # 3.141592653589793 (degrees to radians)
print(help(math))  # Displays help information for the math module
print(math.sin(30))         # 0.49999999999999994 (sine of 30 degrees, radians needed)
